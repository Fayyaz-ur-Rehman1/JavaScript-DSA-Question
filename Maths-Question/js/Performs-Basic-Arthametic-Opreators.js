// Perform basic Arthametic Opreators  take a and b perform Addtion Multiplication Subraction Division and Modules

function Addtion(a, b) {
    return a + b;
}
function Multiplication(a, b) {
    return a * b;

}
function Subraction(a, b) {
    return a - b;
}
function Division(a, b) {
    return a / b;
}
function Modules(a, b) {
    return a % b
}
